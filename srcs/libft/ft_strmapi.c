#include "../../includes/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*result;
	size_t	str_len;
	size_t	i;

	if (!s || !f)
		return (NULL);
	i = 0;
	str_len = ft_strlen(s);
	result = (char *)malloc(sizeof(char) * str_len + 1);
	if (!result)
		return (NULL);
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = 0;
	return (result);
}
